void UARTRX_IRQ()
{
    UART->INTSTATUSnCLEAR = 2;
    char ch = UART_getc();
    if(ch == 'w') uart_w = 1;
    else if(ch == 's') uart_s = 1;
    else if(ch == 'a') uart_a = 1;
    else if(ch == 'd') uart_d = 1;
    else if(ch == 'f') uart_f = 1;
}